package tw.luna.pretty;

public class type01 {

	public static void main(String[] args) {
		// 整數：byte, short, int, long
		byte var1; // 2^8 => -128 ~ 127
		var1 = 12;
		byte var2 = 123;  // [a-zA-Z$_][a-zA-Z0-9$_]*
		byte $_$ = 123;
		System.out.println($_$);
		short var3 = 10000;
		int var4 = 123;
		long var5 = 123;
		
	}

}
