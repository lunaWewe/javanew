package tw.luna.pretty;

public class type03 {

	public static void main(String[] args) {
		// float, double
		float var1 = 10;
		System.out.println(var1);
		float var2 = 10.1f;
		double var3 = 12.3;
		System.out.println(var3);

		boolean var4 = true; // 占用 1 bit
		char c1 = 'a'; // 字元 2^16
		char c2 = 65;
		char c3 = '資';
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);

	}

}
